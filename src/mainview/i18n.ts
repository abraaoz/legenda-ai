/** Traduz uma chave para o idioma da UI, com interpolação de {params}. */
export function translate(lang: string, key: TKey, params?: Record<string, string | number>): string {
  const dict = DICTS[lang as UiLang] ?? en
  let s = (dict[key] ?? en[key]) as string
  if (params) for (const [k, v] of Object.entries(params)) s = s.split(`{${k}}`).join(String(v))
  return s
}

/** Nome legível de um código de idioma (mesma tabela dos idiomas alvo). */
export function langLabel(lang: string, code: string): string {
  const key = `lang_${code}` as TKey
  return (DICTS[lang as UiLang] ?? en)[key] ?? code
}
